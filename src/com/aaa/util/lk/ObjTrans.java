package com.aaa.util.lk;

/**
 * 类型转换
 * 
 * @author S
 * 
 */
public class ObjTrans {

	/**
	 * 将Object 转换为String
	 * 
	 * @param obj
	 * @return
	 */
	public static String toStr(Object obj) {
		String str = "";
		try {
			str = obj.toString();
		} catch (Exception e) {
			e.printStackTrace();
		}
		return str;
	}

	/**
	 * Object转换为Integer
	 * 
	 * @param obj
	 * @return
	 */
	public static Integer toInteger(Object obj) {
		Integer i = null;
		try {
			i = Integer.parseInt(obj.toString());
		} catch (Exception e) {
			e.printStackTrace();
		}
		return i;
	}
}
