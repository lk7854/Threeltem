package com.aaa.entity;

import java.io.Serializable;
import java.util.Date;

/**
 * 机构表
 * 
 * @author Administrator
 * 
 */
public class Institution implements Serializable {

	private static final long serialVersionUID = 1L;
	private Integer in_id;// 机构id
	private String in_name;// 机构名
	private String in_address;// 机构地址
	private String in_certificate;// 机构营业执照
	private String in_man;// 机构负责人
	private Integer u_id;// 修改人id
	private String in_phone;// 负责人电话
	private Integer in_state;// 机构状态
	private Date in_addtime;// 添加时间
	private Date in_updatetime;// 修改时间
	private Integer blid;
	private String in_remark;// 备注

	public Integer getIn_id() {
		return in_id;
	}

	public void setIn_id(Integer in_id) {
		this.in_id = in_id;
	}

	public String getIn_name() {
		return in_name;
	}

	public void setIn_name(String in_name) {
		this.in_name = in_name;
	}

	public String getIn_address() {
		return in_address;
	}

	public void setIn_address(String in_address) {
		this.in_address = in_address;
	}

	public String getIn_certificate() {
		return in_certificate;
	}

	public void setIn_certificate(String in_certificate) {
		this.in_certificate = in_certificate;
	}

	public String getIn_man() {
		return in_man;
	}

	public void setIn_man(String in_man) {
		this.in_man = in_man;
	}

	public Integer getU_id() {
		return u_id;
	}

	public void setU_id(Integer u_id) {
		this.u_id = u_id;
	}

	public Integer getIn_state() {
		return in_state;
	}

	public void setIn_state(Integer in_state) {
		this.in_state = in_state;
	}

	public Date getIn_addtime() {
		return in_addtime;
	}

	public void setIn_addtime(Date in_addtime) {
		this.in_addtime = in_addtime;
	}

	public Integer getBlid() {
		return blid;
	}

	public void setBlid(Integer blid) {
		this.blid = blid;
	}

	public String getIn_remark() {
		return in_remark;
	}

	public void setIn_remark(String in_remark) {
		this.in_remark = in_remark;
	}

	public String getIn_phone() {
		return in_phone;
	}

	public void setIn_phone(String in_phone) {
		this.in_phone = in_phone;
	}

	public Date getIn_updatetime() {
		return in_updatetime;
	}

	public void setIn_updatetime(Date in_updatetime) {
		this.in_updatetime = in_updatetime;
	}

	public static long getSerialversionuid() {
		return serialVersionUID;
	}

	@Override
	public String toString() {
		return "Institution [in_id=" + in_id + ", in_name=" + in_name
				+ ", in_address=" + in_address + ", in_certificate="
				+ in_certificate + ", in_man=" + in_man + ", u_id=" + u_id
				+ ", in_phone=" + in_phone + ", in_state=" + in_state
				+ ", in_addtime=" + in_addtime + ", in_updatetime="
				+ in_updatetime + ", blid=" + blid + ", in_remark=" + in_remark
				+ "]";
	}

	

}
