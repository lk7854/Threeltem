package com.aaa.entity;

import java.io.Serializable;
import java.util.Date;

/**
 * 广告位审核表
 * 
 * @author Administrator
 * 
 */
public class SlotCheck implements Serializable {

	private static final long serialVersionUID = 1L;
	private Integer sc_id;
	private Integer s_id;
	private Integer u_id;
	private Date sc_time;
	private Integer sc_result;
	private String sc_remark;

	public Integer getSc_id() {
		return sc_id;
	}

	public void setSc_id(Integer sc_id) {
		this.sc_id = sc_id;
	}

	public Integer getS_id() {
		return s_id;
	}

	public void setS_id(Integer s_id) {
		this.s_id = s_id;
	}

	public Integer getU_id() {
		return u_id;
	}

	public void setU_id(Integer u_id) {
		this.u_id = u_id;
	}

	public Date getSc_time() {
		return sc_time;
	}

	public void setSc_time(Date sc_time) {
		this.sc_time = sc_time;
	}

	public Integer getSc_result() {
		return sc_result;
	}

	public void setSc_result(Integer sc_result) {
		this.sc_result = sc_result;
	}

	public String getSc_remark() {
		return sc_remark;
	}

	public void setSc_remark(String sc_remark) {
		this.sc_remark = sc_remark;
	}

	public SlotCheck() {
		super();
	}

	public SlotCheck(Integer sc_id, Integer s_id, Integer u_id, Date sc_time,
			Integer sc_result, String sc_remark) {
		super();
		this.sc_id = sc_id;
		this.s_id = s_id;
		this.u_id = u_id;
		this.sc_time = sc_time;
		this.sc_result = sc_result;
		this.sc_remark = sc_remark;
	}

	@Override
	public String toString() {
		return "SlotCheck [sc_id=" + sc_id + ", s_id=" + s_id + ", u_id="
				+ u_id + ", sc_time=" + sc_time + ", sc_result=" + sc_result
				+ ", sc_remark=" + sc_remark + "]";
	}

}
